    private void criarServerSocket(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criarServerSocket
        // TODO add your handling code here:
        int porta = Integer.parseInt(txtPortaServer.getText());

        new Thread(() -> {
            try {
                InetAddress rede = InetAddress.getByName("0.0.0.0");
                serverSocket = new ServerSocket(porta, 0, rede);
                System.out.println("Criado Socket de Conexao.\n");
                txtStatus.setText("ATIVO");
                txtIp.setText(InetAddress.getLocalHost().getHostAddress());
                txtPorta.setText(Integer.toString(porta));
                JOptionPane.showMessageDialog(null, "Servidor criado", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                while (true) {
                    try {
                        // Cria um thread para lidar com a conex√£o do cliente
                        new Servidor(serverSocket.accept(), this);
                        System.out.println("Cliente conectado !");
                    } catch (IOException e) {
                        System.out.println("Conexao Falhou!");
                        System.exit(0);
                    }
                }
            } catch (Exception e) {

                System.out.println("Erro ao iniciar o servidor: " + e.getMessage());
                System.exit(0);
            }
        }).start();

        modalServerSocket.dispose();
    }//GEN-LAST:event_criarServerSocket

