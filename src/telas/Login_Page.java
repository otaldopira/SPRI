    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        try {
            String email = txtEmail.getText();
            String senhaCriptograda = Login.cifraDeCesar(txtSenha.getText());
            Gson gsonFrom = new Gson();

            //Cria uma inst창ncia de Login
            Login login = new Login(email, senhaCriptograda);
            //Transforma em JSON
            String json = new Gson().toJson(login);
            //Cria conex찾o com o servidor
            Socket socket = new Socket(ClienteTCP.getIpConexao(), ClienteTCP.getPortaConexao());
            //Cria um PrintWriter para enviar a string JSON para o servidor
            PrintWriter out = new PrintWriter(socket.getOutputStream(), true);
            //Envia para o servidor
            out.println(json);
            System.out.println("Enviado: " + json);
            //Cria um BufferedReader para receber a resposta do servidor
            BufferedReader in = new BufferedReader(new InputStreamReader(socket.getInputStream()));
            //Recebe a resposta do servidor
            String response = in.readLine();
            System.out.println("Recebido: " + response);
            //Transforma a resposta em JsonObject
            JsonObject jsonObject = gsonFrom.fromJson(response, JsonObject.class);
            //Recupera o status com a mensagem
            String status = jsonObject.get("status").getAsString();

            if (status.equals("OK")) {
                // Salvar os dados do usuario no cliente
                try {
                    Usuario usuario = new Usuario(jsonObject.get("id").getAsInt(), jsonObject.get("nome").getAsString(), email, jsonObject.get("token").getAsString());
                    Usuario.setInstancia(usuario);
                    this.dispose();
                    new Inicio_Page().setVisible(true);
                } catch (NullPointerException e) {
                    JOptionPane.showMessageDialog(null, status, "Erro ao fazer login", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, status, "Erro", JOptionPane.ERROR_MESSAGE);
            }
            out.close();
            in.close();
            // Fecha o socket
            socket.close();

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Erro de conex찾o com o servidor", "Erro", JOptionPane.ERROR_MESSAGE);
            System.out.println("Erro de conex찾o com o servidor");
        }

    }//GEN-LAST:event_btnEntrarActionPerformed

